function getAgo($timestamp){


    $datetime1=new DateTime("now");
    $datetime2=date_create($timestamp);
    $diff=date_diff($datetime1, $datetime2);
    $timemsg='';
    if($diff->y > 0){
        $timemsg = $diff->y .' yıl';
    
    }
    else if($diff->m > 0){
    $timemsg = $diff->m . ' ay';
    }
    else if($diff->d > 0){
    $timemsg = $diff->d .' gün';
    }
    else if($diff->h > 0){
    $timemsg = $diff->h .' saat';
    }
    else if($diff->i > 0){
    $timemsg = $diff->i .' dakika';
    }
    else if($diff->s > 0){
    $timemsg = $diff->s .' saniye';
    }
    if($timemsg){
        $timemsg = $timemsg.' önce';
    }
    else{
        $timemsg='Az önce';
    }
    return $timemsg;

}

