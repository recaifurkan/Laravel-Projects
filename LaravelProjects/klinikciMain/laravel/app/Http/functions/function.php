function getAllSpots($kategoriler){
    $spots = array();
    foreach ($kategoriler as $kategori) {
        foreach ($kategori->dersler as $ders) {
            
            foreach ($ders->uniteler as $unite) {
            foreach ($unite->spotlar  as $spot) {
              array_push($spots,$spot);
            }
        } 
    } 
    }
    return $spots;
}

